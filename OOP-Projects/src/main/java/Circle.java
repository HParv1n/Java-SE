public class Circle extends Shape{

    @Override
    public double sahe(int a, int b) {
        a = 5;
        b = 8;
        return a*b;
    }

    @Override
    public double hecm(int a1, int b1, int c1) {
        a1 = 4;
        b1 = 8;
        c1 = 11;
        return a1*b1*c1;
    }
}
